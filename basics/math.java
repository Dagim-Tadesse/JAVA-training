
public class math {
    public static void main(String[] args) {
        double z = 3.51, y = 4, x = 3;
        System.out.println("math.ceil is " + Math.ceil(z));
        System.out.println("math.floor is " + Math.floor(z));
        System.out.println("math.round is " + Math.round(z));
        System.out.println("the power is " + Math.pow(y, x));
        System.out.println("the maximum is " + Math.max(y, x));
        System.out.println("square root is " + Math.sqrt(y));
        System.out.println("random number is " + (int) (Math.random() * 100) + "%");

    }
}
